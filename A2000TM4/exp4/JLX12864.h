//送指令到晶联讯字库 IC
void send_command_to_ROM(uint8_t datu)
{
    uint8_t i;

    for (i = 0; i < 8; i++)
    {
        LCD_ROM_SCK_L;
        LCD_delay_us(10);

        if (datu & 0x80)
            LCD_ROM_IN_H;
        else
            LCD_ROM_IN_L;

        datu = datu << 1;
        LCD_ROM_SCK_H;
        LCD_delay_us(10);
    }
}

//从晶联讯字库 IC 中取汉字或字符数据（1 个字节）
static uint8_t get_data_from_ROM()
{
    uint8_t i;
    uint8_t ret_data = 0;

    for (i = 0; i < 8; i++)
    {
        LCD_ROM_SCK_L;
        //LCD_delay_us(1);
        ret_data = ret_data << 1;

        if (LCD_ROM_OUT)
            ret_data = ret_data + 1;
        else
            ret_data = ret_data + 0;

        LCD_ROM_SCK_H;
        //LCD_delay_us(1);
    }

    return (ret_data);
}

/**
 * @brief 从指定地址读出数据写到液晶屏指定（page,column)坐标中
 * 
 * @param fontaddr 
 * @param page 行页号
 * @param column 列号
 * @param inverse 是否反白
 */
void get_and_write_16x16(unsigned long fontaddr, uint8_t page, uint8_t column, bool inverse)
{
    uint8_t i, j, disp_data;
    LCD_ROM_CS_L;
    send_command_to_ROM(0x03);
    send_command_to_ROM((fontaddr & 0xff0000) >> 16); //地址的高 8 位,共 24 位
    send_command_to_ROM((fontaddr & 0xff00) >> 8);    //地址的中 8 位,共 24 位
    send_command_to_ROM(fontaddr & 0xff);             //地址的低 8 位,共 24 位

    for (j = 0; j < 2; j++)
    {
        lcd_address(page + j, column);

        for (i = 0; i < 16; i++)
        {
            disp_data = (inverse ? ~get_data_from_ROM() : get_data_from_ROM());
            transfer_data_lcd(disp_data); //写数据到 LCD,每写完 1 字节的数据后列地址自动加 1
        }
    }

    LCD_ROM_CS_H;
}

